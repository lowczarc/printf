#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	if (!s2[0] || !n)
		return (s1);
	s1[ft_strlen(s1) + 1] = 0;
	s1[ft_strlen(s1)] = s2[0];
	return (ft_strncat(s1, &s2[1], n - 1));
}

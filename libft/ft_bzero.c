#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*m;

	i = 0;
	while (i < n)
	{
		m = (unsigned char*)(s + i);
		*m = 0;
		i++;
	}
}

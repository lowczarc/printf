#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;

	i = ft_strlen(s1);
	s1[i] = *s2;
	if (*s2 != 0)
		s1[i + 1] = 0;
	if (*s2)
		ft_strcat(&s1[i], &s2[1]);
	return (s1);
}
